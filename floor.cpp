#include <iostream>
#include <cmath>
using namespace std;

int main()
{
	cout << "Floor is : " << floor(2.3) << endl;		//2
	cout << "Floor is : " << floor(-2.3) << endl;		//-3
	cout << "Floor is : " << floor(11.98) << endl;		//11


	return 0;
}

//floor function returns largest integer smaller than or equal to x
