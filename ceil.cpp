#include <iostream>
#include <cmath>
using namespace std;

int main()
{
	cout << "Ceil is : " << ceil(2.3) << endl;		//3
	cout << "Ceil is : " << ceil(-2.3) << endl;		//-2
	cout << "Ceil is : " << ceil(11.98) << endl;		//12


	return 0;
}

//ceil function returns smallest integer greater than or equal to x
